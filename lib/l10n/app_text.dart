  static String tr(
    BuildContext context,
    String key, {
    Map<String, String>? args,
  }) {
    return translate(
      LanguageService().currentLanguage,
      key,
      args: args,
    );
  }

  static String translate(
    String language,
    String key, {
    Map<String, String>? args,
  }) {
    final langMap = _strings[language] ?? _strings[LanguageService.defaultLanguage]!;
    var value = langMap[key] ??
        _strings[LanguageService.defaultLanguage]![key] ??
        key;

    args?.forEach((placeholder, replacement) {
      value = value.replaceAll('{$placeholder}', replacement);
    });

    return value;
  }

